// average = sum of all the numbers / no. of numbers

// average = sum of 3 numbers / 3
// average = sum of 2 numbers / 2
// average = sum of all the 4 numbers / 4

function average(num1, num2, num3) {
    let result = (num1+num2+num3)/3;
    return result;
}

console.log(average(1,20,4))
console.log(average(3,67,8))